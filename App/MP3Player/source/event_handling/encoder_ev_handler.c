void encoderHandlerGetEvents(encoder_event_t *encoder_event) {
	if(EncoderEventAVB(0)==EVENT_AVB){
		if(EncoderPopEvent(0) == LEFT_TURN){
			encoder_event->back_button=true;
		}
		else{
			encoder_event->next_button=true;
		}
	}
}

